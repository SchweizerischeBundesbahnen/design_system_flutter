/// Spacing scale for SBB Design System.
class SBBSpacing {
  SBBSpacing._();

  /// 0 logical pixels
  static const double zero = 0.0;

  /// Corresponds to 4 logical pixels.
  static const double xxSmall = 4.0;

  /// Corresponds to 8 logical pixels.
  static const double xSmall = 8.0;

  /// Corresponds to 12 logical pixels.
  static const double small = 12.0;

  /// Corresponds to 16 logical pixels.
  static const double medium = 16.0;

  /// Corresponds to 24 logical pixels.
  static const double large = 24.0;

  /// Corresponds to 32 logical pixels.
  static const double xLarge = 32.0;
}
